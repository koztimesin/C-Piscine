#include <unistd.h>
#include <stdio.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_putstr(char *c)
{
	int	i;

	i = 0;
	while (c[i])
	{
		ft_putchar(c[i]);
		i++;
	}
}

int	main(int argc, char **argv)
{	
	while (argc)
	{
		ft_putstr(argv[argc - 1]);
		argc--;
	}
	ft_putstr("\n");
}
