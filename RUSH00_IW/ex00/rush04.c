void	ft_putchar(char a, char b, char c, int n);

void	rush04(int x, int y)
{
	if (y >= 1)
	{
		ft_putchar ('A', 'B', 'C', x);
		while (y - 2 > 0)
		{
			ft_putchar ('B', ' ', 'B', x);
			y--;
		}
		if (y > 1)
			ft_putchar ('C', 'B', 'A', x);
	}
}
