#include <unistd.h>

void	ft_putchar(char a, char b, char c, int n)
{
	if (n >= 1)
	{
		write (1, &a, 1);
		while (n - 2 > 0)
		{
			write(1, &b, 1);
			n--;
		}
		if (n > 1)
			write(1, &c, 1);
		write(1, "\n", 1);
	}
}
